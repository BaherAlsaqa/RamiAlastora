package com.ramyhd.ramialastora.classes;

public class RecommendedNews {
    public int image;
    public String title;

    public RecommendedNews(int image, String title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
